#methods are list specific functions that perform operations on lists in Python.
# Here are some common list methods with examples:
marks = [99, 85, 76, 88]
marks.append(89)  # Adds 89 to the end of the list
print("After append:", marks)
marks.remove(76)  # Removes the first occurrence of 76
print("After remove:", marks)
marks.sort()      # Sorts the list in ascending order
print("After sort:", marks)
marks.reverse()   # Reverses the list
print("After reverse:", marks)
marks.insert(2, 95)  # Inserts 95 at index 2
print("After insert:", marks)
